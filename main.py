#1. Create a greeting for your program.
print("Hi there. Welcome to the band name generator!")

#2. Ask the user for the city that they grew up in.
city = input("Please enter the name of the city you grew up in: ")

#3. Ask the user for the name of a pet.
pet = input("Please enter the name of your pet or a pet you know: ")

#4. Combine the name of their city and pet and show them their band name.
band_name = city + " " + pet

#5. Make sure the input cursor shows on a new line:
print("\nYour band name is " + band_name)

